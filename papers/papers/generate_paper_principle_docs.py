def render_paper_md(paper: dict) -> str:
    def bullets(items):
        return "\n".join(f"- {item}" for item in items)

    ext = ENHANCEMENTS.get(paper["slug"], {})
    formula_items = ext.get("formula", ["-"])
    prereq_items = ext.get("prereq", ["`Attention Is All You Need`"])
    next_items = ext.get("next_reading", ["`与你当前方向最相关的下一篇论文`"])

    sections = [
        f'# {paper["short"]} 原理导读',
        "",
        f'**论文标题**：`{paper["title"]}`',
        f'**年份**：`{paper["year"]}`',
        f'**主题**：`{paper["category"]}`',
        f'**定位**：{paper["position"]}',
        f'**论文链接**：[{ext.get("link_label", "Source")}]({ext.get("link", "")})',
        "",
        "## 1. 代表公式 / 关键表达",
        bullets(formula_items),
        "",
        "## 2. 这篇论文要解决什么问题",
        bullets(paper["problem"]),
        "",
        "## 3. 核心思想",
        bullets(paper["ideas"]),
        "",
        "## 4. 方法原理拆解",
        bullets(paper["mechanism"]),
        "",
        "## 5. 训练或推理范式",
        bullets(paper["training"]),
        "",
        "## 6. 为什么它有效",
        bullets(paper["why_work"]),
        "",
        "## 7. 局限与争议",
        bullets(paper["limits"]),
        "",
        "## 8. 对后续研究的影响",
        bullets(paper["impact"]),
        "",
        "## 9. 你应该怎样读这篇",
        bullets(paper["tips"]),
        "",
        "## 10. 前置阅读",
        bullets(prereq_items),
        "",
        "## 11. 读完接着看",
        bullets(next_items),
        "",
    ]
    return "\n".join(sections)


def render_readme(papers: list[dict]) -> str:
    lines = [
        "# 论文原理文档索引",
        "",
        "本目录为 `transformer_llm_papers_guide_zh.md` 中 30 篇主论文的独立详细原理文档。",
        "增强版已补充：`论文链接`、`代表公式 / 关键表达`、`前置阅读`、`读完接着看`。",
        "",
        "## 文件列表",
        "",
    ]
    for paper in papers:
        filename = f'{paper["num"]:02d}_{paper["slug"]}_原理.md'
        lines.append(
            f'- `{filename}`: `{paper["short"]}` - {paper["category"]}'
        )
    lines += [
        "",
        "## 建议阅读顺序",
        "",
        "1. 预训练主线：`BERT -> GPT -> GPT-2 -> T5 -> GPT-3`",
        "2. 扩展律主线：`Scaling Laws -> Chinchilla -> PaLM`",
        "3. 对齐主线：`FLAN -> InstructGPT -> Constitutional AI -> DPO`",
        "4. 推理主线：`Chain-of-Thought -> Self-Consistency -> Let's Verify Step by Step -> Tree of Thoughts -> DeepSeek-R1`",
        "5. 工程主线：`RoPE -> FlashAttention -> LoRA -> QLoRA -> LLaMA -> Mixtral`",
        "6. Agent / 检索 / 多模态补充：`RAG -> Toolformer -> ReAct -> Gemini -> Mamba`",
        "",
        "## 说明",
        "",
        "- 每份文档都采用统一结构，便于横向比较。",
        "- 每篇均补了链接与代表公式，适合做逐篇精读起点。",
        "- 内容聚焦原理、方法逻辑、影响和阅读建议，不追求逐节翻译原论文。",
        "- 如果后续需要，我可以继续补每篇的逐节拆解版、图解版或公式推导版。",
        "",
    ]
    return "\n".join(lines)


def main():
    OUT_DIR.mkdir(exist_ok=True)
    for paper in PAPERS:
        filename = OUT_DIR / f'{paper["num"]:02d}_{paper["slug"]}_原理.md'
        filename.write_text(render_paper_md(paper), encoding="utf-8")

    (OUT_DIR / "README.md").write_text(render_readme(PAPERS), encoding="utf-8")


if __name__ == "__main__":
    main()
